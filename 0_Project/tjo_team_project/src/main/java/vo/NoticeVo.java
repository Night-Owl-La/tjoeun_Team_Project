package vo;

public class NoticeVo {
	int notice_idx;
	String notice_category;
	String notice_title;
	String notice_content;
	String notice_view_count;
	String notice_regdate;
	String notice_modifydate;
	String notice_author;

	public int getNotice_idx() {
		return notice_idx;
	}

	public void setNotice_idx(int notice_idx) {
		this.notice_idx = notice_idx;
	}

	public String getNotice_category() {
		return notice_category;
	}

	public void setNotice_category(String notice_category) {
		this.notice_category = notice_category;
	}

	public String getNotice_title() {
		return notice_title;
	}

	public void setNotice_title(String notice_title) {
		this.notice_title = notice_title;
	}

	public String getNotice_content() {
		return notice_content;
	}

	public void setNotice_content(String notice_content) {
		this.notice_content = notice_content;
	}

	public String getNotice_view_count() {
		return notice_view_count;
	}

	public void setNotice_view_count(String notice_view_count) {
		this.notice_view_count = notice_view_count;
	}

	public String getNotice_regdate() {
		return notice_regdate;
	}

	public void setNotice_regdate(String notice_regdate) {
		this.notice_regdate = notice_regdate;
	}

	public String getNotice_modifydate() {
		return notice_modifydate;
	}

	public void setNotice_modifydate(String notice_modifydate) {
		this.notice_modifydate = notice_modifydate;
	}

	public String getNotice_author() {
		return notice_author;
	}

	public void setNotice_author(String notice_author) {
		this.notice_author = notice_author;
	}

}
