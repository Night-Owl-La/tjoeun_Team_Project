package vo;

public class RegScreenVo {
	String docid;
	String reg_title;
	String reg_rating;
	int reg_price;

	public String getDocid() {
		return docid;
	}

	public void setDocid(String docid) {
		this.docid = docid;
	}

	public String getReg_title() {
		return reg_title;
	}

	public void setReg_title(String reg_title) {
		this.reg_title = reg_title;
	}

	public String getReg_rating() {
		return reg_rating;
	}

	public void setReg_rating(String reg_rating) {
		this.reg_rating = reg_rating;
	}

	public int getReg_price() {
		return reg_price;
	}

	public void setReg_price(int reg_price) {
		this.reg_price = reg_price;
	}

}
